package nl.bzk.brp.generatoren.java.writer.formatter;

/**
 * Een Eclipse formatter configuratie setting, zoals deze wordt opgegeven in Eclipse formatter configuraties.
 */
public class ProfielSetting {

    private String id;
    private String value;

    /**
     * Constructor.
     */
    public ProfielSetting() {
    }

    public String getId() {
        return id;
    }

    public void setId(final String id) {
        this.id = id;
    }

    public String getValue() {
        return value;
    }

    public void setValue(final String value) {
        this.value = value;
    }

}
