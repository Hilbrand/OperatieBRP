package nl.bzk.brp.levering.lo3.mapper;

import nl.bzk.brp.gba.dataaccess.VerConvRepository;
import nl.bzk.brp.model.algemeen.stamgegeven.kern.SoortPersoon;
import nl.bzk.brp.model.hisvolledig.predikaatview.kern.PersoonHisVolledigView;
import nl.bzk.brp.util.hisvolledig.kern.PersoonHisVolledigImplBuilder;
import nl.bzk.migratiebrp.conversie.model.brp.BrpStapel;
import nl.bzk.migratiebrp.conversie.model.brp.groep.BrpVerblijfsrechtInhoud;
import org.junit.Assert;
import org.junit.Before;
import org.junit.Test;
import org.mockito.Mockito;
import org.springframework.test.util.ReflectionTestUtils;

public class VerblijfsrechtMapperTest {

    private final VerblijfsrechtMapper mapper = new VerblijfsrechtMapper();

    @Before
    public void setup() {
        final VerConvRepository dummyVerConvRepository = Mockito.mock(VerConvRepository.class, Mockito.RETURNS_DEFAULTS);
        ReflectionTestUtils.setField(mapper, "verConvRepository", dummyVerConvRepository);
    }

    @Test
    public void testSucces() {
        final PersoonHisVolledigImplBuilder builder = new PersoonHisVolledigImplBuilder(SoortPersoon.INGESCHREVENE);
        final PersoonHisVolledigView persoonHisVolledig = new PersoonHisVolledigView(maak(builder).build(), null);
        final BrpStapel<BrpVerblijfsrechtInhoud> brpVerblijfstitel =
                mapper.map(persoonHisVolledig, new OnderzoekMapper(persoonHisVolledig), new TestActieHisVolledigLocator());

        Assert.assertNotNull(brpVerblijfstitel);
        Assert.assertTrue(brpVerblijfstitel.size() == 1);
        Assert.assertEquals(Short.valueOf((short) 1), brpVerblijfstitel.get(0).getInhoud().getAanduidingVerblijfsrechtCode().getWaarde());
    }

    @Test
    public void testLeeg() {
        final PersoonHisVolledigImplBuilder builder = new PersoonHisVolledigImplBuilder(SoortPersoon.INGESCHREVENE);
        final PersoonHisVolledigView persoonHisVolledig = new PersoonHisVolledigView(builder.build(), null);
        final BrpStapel<BrpVerblijfsrechtInhoud> brpVerblijfstitel =
                mapper.map(persoonHisVolledig, new OnderzoekMapper(persoonHisVolledig), new TestActieHisVolledigLocator());

        Assert.assertNull(brpVerblijfstitel);
    }

    @Test
    public void testGeenTitel() {
        final PersoonHisVolledigImplBuilder builder = new PersoonHisVolledigImplBuilder(SoortPersoon.INGESCHREVENE);
        builder.nieuwVerblijfsrechtRecord(MapperTestUtil.maakActieModel(20131210000000L, 20131211, 20141212)).eindeRecord();
        final PersoonHisVolledigView persoonHisVolledig = new PersoonHisVolledigView(builder.build(), null);
        final BrpStapel<BrpVerblijfsrechtInhoud> brpVerblijfstitel =
                mapper.map(persoonHisVolledig, new OnderzoekMapper(persoonHisVolledig), new TestActieHisVolledigLocator());

        Assert.assertNotNull(brpVerblijfstitel);
        Assert.assertTrue(brpVerblijfstitel.size() == 1);
        Assert.assertNull(brpVerblijfstitel.get(0).getInhoud().getAanduidingVerblijfsrechtCode());
    }

    public static PersoonHisVolledigImplBuilder maak(final PersoonHisVolledigImplBuilder builder) {
        // @formatter:off
        return builder.nieuwVerblijfsrechtRecord(MapperTestUtil.maakActieModel(20131210000000L, 20131211, 20141212))
                .datumMededelingVerblijfsrecht(20131211)
                .datumVoorzienEindeVerblijfsrecht(20141211)
                .aanduidingVerblijfsrecht((short) 1)
                .eindeRecord();
        // @formatter:on
    }
}
